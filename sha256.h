#include <stdint.h>

typedef struct {
  uint32_t sz[2];
  uint32_t counter[8];
  union {
      unsigned char save_bytes[64];
      uint32_t save_u32[16];
  } u;
} sha256_ctx;

void sha256_init(sha256_ctx *m);
void sha256_update(sha256_ctx *m, const void *v, uint32_t len);
void sha256_final_32bytes(sha256_ctx *m, uint8_t result[32]);
